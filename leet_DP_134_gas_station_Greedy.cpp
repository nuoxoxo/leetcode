class Solution {
public:
    int canCompleteCircuit(vector<int>& Gas, vector<int>& Cost)
    {
        int     i, res, gas, tank;

        i = -1;
        gas = tank = res = 0;
        while (++i < Gas.size())
        {
            tank += Gas[i] - Cost[i];
            gas += Gas[i] - Cost[i];
            if (gas < 0)
            {
                gas = 0;
                res = i + 1;
            }
        }
        return (tank > -1 ? res : -1);
    }
};

