class Solution:
    def majorityElement(self, nums: List[int]) -> int:
        r = 1e9
        c = 1
        for n in nums:
            if r != n:
                c -= 1
                if c == 0:
                    r = n
                    c = 1
            else:
                c += 1
        return r
