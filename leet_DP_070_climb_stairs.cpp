class Solution {
public:
    int climbStairs(int n)
    {
        int	arr[n + 1], i;

        if (n < 3)  return (n);
        arr[0] = 1;
        arr[1] = 1;
        arr[2] = 2;
        i = 2;
        while (++i < n + 1)
		arr[i] = arr[i - 1] + arr[i - 2];

        return (arr[n]);
    }
};
