class Solution {
public:
    int calculate(string str)
    {
        char    c, *s;
        int     f, a, res;

        s = (char *) str.c_str();
        f = strtol(s, &s, 10);
        res = 0;
        while(*s)
        {
            while( *s == ' ' )
                s++;
            c = *s;
            a = strtol(++s, &s, 10);
            if (c == '+' || c == '-')
            {
                if ( c == '-' )
                    a = -a;
                res += f;
                f = a;
            }
            if ( c == '*' )
                f *= a;
            if ( c == '/' )
                f /= a;
        }
        return (res + f);
    }
};
