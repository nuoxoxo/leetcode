class Solution {
public:
    int getDecimalValue(ListNode* head)
    {
        ListNodd    *p;
        string      s;
        
        p = head;
        while (p)
        {
            s += p->val + '0';
            p = p->next;
        }
        return (stoi(s, 0, 2));
    }
};

