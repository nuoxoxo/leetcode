# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right

class Solution:
    def maxDepth(self, node: Optional[TreeNode]) -> int:
        return self.maxD(node, 0)

    def maxD(self, node, depth) -> int:
        if node is None:
            return depth
        L = self.maxD(node.left, depth + 1)
        R = self.maxD(node.right, depth + 1)
        return L if L > R else R
