class Solution {
public:
    bool judgeCircle(string moves)
    {
        int i = -1, V = 0, H = 0;
        while (++i < moves.length())
        {
            char    c = moves[i];

            if (c == 'L')   -- H;
            if (c == 'R')   ++ H;
            if (c == 'U')   ++ V;
            if (c == 'D')   -- V;
        }
        return (!H && !V);
    }
};
