class   Solution 
{
    public:

    int     myAtoi(string s)
    {
            int             sign, i;
            long            tt;

            if ( !s.length())   return (0);

            sign = 1;
            tt = i = 0;
            while (s[i] == ' ' || (s[i] < 14 && s[i] > 8))
                i++;
            if (s[i] == '+')
                i++;
            else if (s[i] == '-')
            {
                sign *= -1;
                i++;
            }
            while (s[i] <= '9' && s[i] >= '0')
            {
                tt *= 10;
                tt += s[i] - '0';
                if (tt > 2147483647) 
                {
                    if (sign < 0) return (-2147483648);
                    return (2147483647);
                }
                i++;
            }
            return (tt * sign);
    }
};
